    public MyStack() {
        q1 = new LinkedList<>();
        q2 = new LinkedList<>();
    }
    
    /** Push element x onto stack. */
    public void push(int x) {
        if(empty())
            q1.offer(x);
        else
        {
            while(!q1.isEmpty())
                q2.offer(q1.poll());
            q1.offer(x);
            while(!q2.isEmpty())
                q1.offer(q2.poll());
        }
    }
    
    /** Removes the element on top of the stack and returns that element. */
    public int pop() {
        return q1.poll();
    }
    
